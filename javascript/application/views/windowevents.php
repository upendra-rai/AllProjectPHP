The highly civilized Harappans knew the art of growing cereals, and wheat and barley constituted their staple food. They consumed vegetables and fruits and ate mutton, pork and eggs as well. Evidences also show that they wore cotton as well as woollen garments. By 1500 BC, the Harappan culture came to an end. Among various causes ascribed to the decay of Indus Valley Civilization are the recurrent floods and other natural causes like earthquake, etc.</p>
